#include "gpio.h"

/*****************************************
*  00 准双向io口                01 推挽输出
*  10 高阻输入                  11 开漏
*****************************************/
void gpio_init(void)   //按键，数码管，蜂鸣器，LED等端口初始化
{					   //其中按键P11为高阻输入,红外P00，串口收P30，串口发P31待定外，其余均为推挽输出
	P0M0 = 0x00 ; //0000 0000
	P0M1 = 0xfe ; //1111 1110
	
	P1M0 = 0x02 ; //0000 0010
	P1M1 = 0xfd ; //1111 1101
	
	P2M0 = 0x00 ; //0000 0000
	P2M1 = 0xff ; //1111 1111

	P3M0 = 0x00 ; //0000 0000
	P3M1 = 0xfc ; //1111 1100
}